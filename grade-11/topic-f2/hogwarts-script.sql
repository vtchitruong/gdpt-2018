-- create database hogwarts;

-- Bảng môn học
create table subjects (
    subject_id char(3) primary key,
    subject_name varchar(50) NOT NULL,
    subject_type varchar(20),
    minimum_year int default 1,
    subject_description text
);

-- Bảng thần chú
create table spells (
	spell_id serial primary key,
    spell_name varchar(100),
    incantation varchar(50),
    spell_type varchar(50),
    effect text,
    light varchar(50),
    difficulty_level varchar(20)
);
    
-- Bảng chủng loại sinh vật
create table species (
	spec_id char(2) primary key,
    spec_name varchar(50) not null,
    spec_description text
);

-- Bảng độc dược
create table potions (
	potion_id serial primary key,
    potion_name varchar(100) not null,
    ingredients text,
    effect text,
    characteristics text,
    difficulty_level varchar(50),
    brewing_time varchar(50)
);

-- Bảng nhà
create table houses (
	house_id char(1) primary key,
    house_name varchar(50) not null,
    head varchar(100),
    ghost varchar(100),
    founder varchar(100),
    traits text,
    emblematic_animal varchar(20),
    color_1 varchar(20),
    color_2 varchar(20),
    nature_element varchar(10)
);

-- Bảng các loại điểm kiểm tra
create table grades (
	grade_id char(1) primary key,
	grade_name varchar(50) not null,
    grade_type varchar(10) not null,
    grade_description text
);

-- Bảng loại huyết thống
create table bloods (
	blood_id char(2) primary key,
	blood_name varchar(13),
    blood_description varchar(97)
);

-- Bảng nhân vật
create table characters (
	character_id char(5) primary key,
    first_name varchar(50) not null,
    middle_name varchar(50),
    last_name varchar(50),
    house_id char(1),
    gender varchar(10),
    birth_date date,
    death_date date,
    patronus varchar(50),
    boggart char(50),
    blood_id char(2),
    spec_id varchar(2),
    animagus varchar(50),
    job varchar(150),
    hair_color varchar(30),
    eye_color varchar(30),
    loyalty text,
    skills text,
	foreign key (house_id) references houses (house_id),
    foreign key (blood_id) references bloods (blood_id),
    foreign key (spec_id) references species (spec_id)
);

-- Bảng đũa thần
create table wands (
    wand_id serial primary key,
    character_id char(5),
    wood varchar(50),
    core varchar(50),
    length_inches decimal(4,2),
    flexibility varchar(50),
    FOREIGN KEY (character_id) REFERENCES characters(character_id)
);

