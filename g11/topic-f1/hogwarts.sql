-- Tạo bảng chứa dữ liệu các môn học
create table subjects (
    subject_id char(3) primary key,
    subject_name varchar(29),
    subject_type varchar(7)
);

-- Tạo bảng chứa dữ liệu các câu thần chú
create table spells (
	spell_id smallint primary key,
    spell_name varchar(40),
    incantation varchar(23),
    spell_type varchar(33),
    effect varchar(127),
    light varchar(45)
);
    
-- Tạo bảng chứa dữ liệu các chủng loại sinh vật
create table species (
	spec_id char(2) primary key,
    spec_name varchar(10),
    spec_description varchar(106)
);

-- Tạo bảng chứa dữ liệu các độc dược
create table potions (
	potion_id smallint primary key,
    potion_name varchar(100),
    ingredients varchar(191),
    effect varchar(142),
    features varchar(105),
    difficulty_level varchar(49)
);

-- Tạo bảng chứa dữ liệu các nhà
create table houses (
	house_id char(1) primary key,
    house_name varchar(28),
    head varchar(18), ghost varchar(20),
    founder varchar(17),
    trait varchar(92),
    emblematic_animal varchar(7),
    color_1 varchar(7),
    color_2 varchar(6),
    nature_element varchar(5)
);

-- Tạo bảng chứa dữ liệu các loại điểm kiểm tra
create table grades (
	grade_id char(1) primary key,
	grade_name varchar(20),
    grade_type varchar(4)
);

-- Tảo bảng chứa dữ liệu các loại huyết thống
create table bloods (
	blood_id char(2) primary key,
	blood_name varchar(13),
    blood_description varchar(97)
);

-- Tạo bảng chứa dữ liệu các nhân vật
create table characters (
	character_id char(5) primary key,
    first_name varchar(20),
    middle_name varchar(22),
    last_name varchar(22),
    house_id char(1),
    gender boolean,
    birth_date varchar(10),
    birth_date_string varchar(45),
    death_time varchar(29),
    patronus varchar(20),
    boggart char(20),
    blood_id char(2),
    animagus varchar(11),
    wand_1 varchar(86),
    wand_2 varchar(35),
    wand_3 varchar(35),
    spec_id varchar(2),
    job varchar(100),
    hair_color varchar(29),
    eye_color varchar(27),
    loyalty varchar(104),
    skills varchar(138),
	foreign key (house_id) references houses (house_id),
    foreign key (blood_id) references bloods (blood_id),
    foreign key (spec_id) references species (spec_id)
);

-- Tạo bảng chứa dữ liệu học tập và điểm số của các nhân vật
create table learning (
	character_id char(5),
    subject_id char(3),
    test_grade char(1)
);

